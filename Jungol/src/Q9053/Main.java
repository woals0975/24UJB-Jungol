package Q9053;

public class Main {
	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=5;j++) {
			System.out.print(i+" ");
			if(j==5) {
				System.out.print("\n");
			}
		}
	}
		System.out.print("\n");
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=5;j++) {
			System.out.print(j+" ");
			if(j==5) {
				System.out.print("\n");
			}
		}
	}

}
}
