package Q9067;

import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int[] a = new int[10];
		
		for(int i=0; i<10;i++) {
			int b = sc.nextInt();
			a[i] = b;
		
			if(i==2) {
				System.out.print(a[i]+" ");
			}else {}
			if(i==4) {
				System.out.print(a[i]+" ");
			}else {}
			if(i==9) {
				System.out.print(a[i]+" ");
				break;
			}else {}


		}
			
		
		}
	}

